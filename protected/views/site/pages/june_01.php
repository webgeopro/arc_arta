<h2>Семейный праздник в честь Дня защиты детей</h2>
<p>
    1 июня в «Арене.Север» весь день проходит спортивно-развлекательный семейный праздник для детей и родителей.
    Конкурсы, игры, показательные выступления спортсменов, мастер-классы на льду, прыжки на батутах и катание
    на коньках (детям до 14 лет— бесплатно), аниматоры, экскурсия по «Арене.Север» и хоккейный матч детских команд.
</p>
<h2>Начало праздника в 10.00.</h2>

<span style="font-size:18px;font-weight:bolder;text-decoration:underline;">
    Развлекательные площадки:
</span>
<ul style="list-style-type: disc;">
    <li>Батуты — для всех желающих.</li>
    <li>Стол для лепки.</li>
    <li>«Фейс-арт»: детская площадка, где опытный мастер наносит забавную раскраску на лица детям и взрослым.</li>
    <li>Шарики-игрушки. Клоун-аниматор делает из воздушных шаров забавных зверушек и человечков.</li>
    <li>«Дерево желаний» — волшебная яблоня, на яблоках которой дети пишут яркими маркерами заветные
    желания. На фоне дерева можно сфотографироваться — а снимки чуть позже получить в холле «Арены.Север»</li>
</ul>
<span style="font-size:18px;font-weight:bolder;text-decoration:underline;">
    В программе праздника
</span>
<ul style="list-style-type: disc;">
