    <li>Спортивно-приключенческая игра «Сокровища Севера»: 7 команд отправляются с картами на поиск сокровищ: искателей ждут спортивные задания и загадки мудрого старца.</li>
    <li>Конкурс рисунков на асфальте</li>
    <li>Конкурс «Забей шайбу». Каждый ребенок может попробовать себя в роли центрального нападающего.</li>
    <li>Экскурсия по «Арене.Север».</li>
    <li>Показательные выступления спортсменов-скалолазов на скалодроме.</li>
</ul>
